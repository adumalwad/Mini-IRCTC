            if (new.ticket_status='CANCELLED' AND datediff(new.journey_date,curdate())>=0 )then
                if datediff(new.journey_date,curdate())>=10 then 
                    INSERT INTO cancel_ticket values (new.pnr_number,new.ticket_fare1);
